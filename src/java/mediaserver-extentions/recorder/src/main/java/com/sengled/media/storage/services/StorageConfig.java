package com.sengled.media.storage.services;

public class StorageConfig {
    private String token;
    private long userID;
    public String getToken() {
        return token;
    }
    public void setToken(String token) {
        this.token = token;
    }
    public long getUserID() {
        return userID;
    }
    public void setUserID(long userID) {
        this.userID = userID;
    }
    
    
}
